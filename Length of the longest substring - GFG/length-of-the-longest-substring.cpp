class Solution{
    public:
    int longestUniqueSubsttr(string s){
        //code
        //unordered_map<char, int> chars;
        int chars[256]={0};
        int left = 0;
        int right = 0;

        int res = 0;
        while (right < s.length()) {
            char r = s[right];
            chars[r]++;

            while (chars[r] > 1) {
                char l = s[left];
                chars[l]--;
                left++;
            }

            res = max(res, right - left + 1);

            right++;
        }

        return res;
    }
};

